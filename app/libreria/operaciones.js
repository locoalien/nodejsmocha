this.suma = function(a,b){
    var resultado = a+b;
    return resultado;
};

this.resta = function(a,b){
    var resultado = a-b;
    return resultado;
};

this.multiplicar = function(a,b){
    var resultado = a*b;
    return resultado;
};

this.dividir = function(a,b){
    var resultado = a/b;
    return resultado;
};

